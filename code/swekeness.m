function y = swekeness(x,B,n)
    y1=x(1).*B(1,1); % 初始化为第一个变量
    y2=x(1).*B(1,2);
    y3=x(1).*B(1,3);
    for i=2:n
        y1=y1+x(i).*B(i,1); % 与下一个变量相乘
        y2=y2+x(i).*B(i,2);
        y3=y3+x(i).*B(i,3);
    end
     y=1/1080*(-19*y1.^3-45*y1.^2*y2-15*y1.^2*y3+15*y1*y3^2+45*y2*y3^2+19*y3^3);
end

