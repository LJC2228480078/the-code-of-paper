%第1小问，X的第一列为取定的步数，Y的第二列为对应的期权价格
H=[50;100;200;400;800;1000];
for i=1:length(H(:,1))
    X(i,1)=H(i,1);%步数值
    X(i,2)=dieshiqiquan(100,0.25,0.03,1,X(i,1));%计算出的期权价格
end

%第2小问，Y的第一列为股票的当前价格，Y的第二列为对应的期权价格
S=50:1:150;%让股票的当前价格取50到150之间的整数值
for i=1:length(S(1,:))
    s=S(1,i);
    Y(i,1)=s;%股票的当前价格
    Y(i,2)=dieshiqiquan(s,0.25,0.03,1,1000);%计算出的期权价格
end
plot(S,Y(:,2),"-.","LineWidth",1);
xlabel("股票当前价格S");
ylabel("期权价格");
